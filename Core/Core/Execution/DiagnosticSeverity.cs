namespace BeltRunner.Core.Execution;

/// <summary>
/// Represents the severity of a diagnostic entry.
/// </summary>
public enum DiagnosticSeverity {
    /// <summary>
    /// Informational message.
    /// </summary>
    Information = 0,

    /// <summary>
    /// Warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// Error message.
    /// </summary>
    Error
}
